package com.telenav.sdk.demo.automation

import androidx.test.espresso.IdlingResource
import com.telenav.sdk.examples.automation.SimpleCountingIdlingResource

/**
 * Contains a static reference to [IdlingResource]
 */

object EspressoIdlingResource {

    private val RESOURCE = "GLOBAL"

    @JvmField val countingIdlingResource = SimpleCountingIdlingResource(RESOURCE)

    fun increment() {
        countingIdlingResource.increment()
    }

    fun decrement() {
        countingIdlingResource.decrement()
    }
}
